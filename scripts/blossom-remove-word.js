#!/usr/bin/env node
// Pull one or more words out of Blossom's word banks and rebuild the served
// word list. Use this whenever a word needs to disappear from the game — an
// offensive term we missed, a proper noun that slipped through, anything someone
// complains about:
//
//   node scripts/blossom-remove-word.js <word> [word2 ...]
//   make blossom-remove-word word="foo bar"
//
// Each word is matched exactly (case-insensitive, whole line) and deleted from:
//   • assets/blossom/word_bank.txt  — the generation pool (tracked in git)
//   • assets/blossom/scowl-60.txt   — the validation pool source (gitignored,
//                                     local-only; skipped with a warning if
//                                     absent, since it's re-fetched on demand)
// then assets/blossom/words.js is regenerated by invoking the build script.
//
// We remove from the *sources* rather than maintaining a separate blocklist:
// that keeps the curation durable and avoids a standing list of pulled slurs
// living in the repo. A word must be gone from BOTH banks to leave validation,
// since the build merges the gen pool into the validation set — so this always
// edits both.

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const ROOT = path.join(__dirname, '..');
const WORD_BANK_PATH = path.join(ROOT, 'assets/blossom/word_bank.txt');
const SCOWL_PATH = path.join(ROOT, 'assets/blossom/scowl-60.txt');
const BUILD_SCRIPT = path.join(__dirname, 'blossom-build-words.js');

const words = process.argv.slice(2).map(w => w.trim().toLowerCase()).filter(Boolean);
if (!words.length) {
  console.error('Usage: node scripts/blossom-remove-word.js <word> [word2 ...]');
  process.exit(1);
}
const targets = new Set(words);

// Drop every line that exactly matches a target (case-insensitive). Removing
// case-insensitively also strips a capitalized variant if SCOWL carries one
// (e.g. "March"/"march"), which is what we want when pulling a word entirely.
function removeFrom(file, label) {
  if (!fs.existsSync(file)) {
    console.warn(`  ${label}: not found — skipped (it's gitignored / fetched on demand)`);
    return 0;
  }
  const lines = fs.readFileSync(file, 'utf8').split('\n');
  let removed = 0;
  const kept = lines.filter(line => {
    if (targets.has(line.trim().toLowerCase())) {
      removed++;
      return false;
    }
    return true;
  });
  if (removed) fs.writeFileSync(file, kept.join('\n'));
  console.log(`  ${label}: removed ${removed} line(s)`);
  return removed;
}

console.log(`Removing from word banks: ${words.join(', ')}`);
const removed =
  removeFrom(WORD_BANK_PATH, 'word_bank.txt') +
  removeFrom(SCOWL_PATH, 'scowl-60.txt ');

if (removed === 0) {
  console.log('Nothing matched — word lists unchanged, skipping rebuild.');
  process.exit(0);
}

console.log('Rebuilding assets/blossom/words.js …');
execFileSync('node', [BUILD_SCRIPT], { stdio: 'inherit' });
